package day21_WhileLoop;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if(true){
            System.out.println("Hello Batch 21"); // runs the condition once
        }
        System.out.println("===========================");

        while(true){
            System.out.println("Hello Batch 21"); // runs condition infinitely, repeated if statement
        }

    }
}
