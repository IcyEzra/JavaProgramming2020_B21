package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello Everyone");
        System.out.println("My name is Uzair, I am from Brooklyn");

        System.out.println("=========================================");

        //example of \n

        System.out.println("Hello everyone\nMy name is Uzair,\nI am from Brooklyn");

        System.out.println("=========================================================");

        // next escape sequence \t - gives a double space
        System.out.println("\tToday i learned Java");
        System.out.println("it was so much fun");
        System.out.println("==================================================");

        // next escape sequence \\ (double backslash) used for single backlash character /ex)
        System.out.println("/"); //prints single front backslash
        System.out.println("\\");//prints single backslash
        System.out.println("==========================================================");

        //next Escape Sequence: \'    (dont really see the point in this one)
        System.out.println("'Uzair'");
        System.out.println("\'Uzair\'");





    }



  /* Hello everyone
  My name is Uzair, I am from Brooklyn.
   */

}
